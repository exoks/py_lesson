# ===[ Imports: ]==============================================================
import numpy as np
import matplotlib.pyplot as plt


# ===[ main: ]=================================================================
def z(x, y):
    return (x**2 + y**2)


def main():
    x = np.linspace(-3, 3, 100)

    y = np.linspace(-3, 3, 100)

    X, Y = np.meshgrid(x, y)
    np.set_printoptions(precision=2)
    print(X)
    Z = z(X, Y)  # ‚úÖ 2D array

    fig = plt.figure()
    ax = fig.add_subplot(1, 1, 1)
    ax.contour(x, y, Z, levels=10)

    plt.show()


if __name__ == '__main__':
    main()
