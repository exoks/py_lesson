# ===[ Imports: ]==============================================================
import pandas as pd


# ===[ main ]==================================================================
def main():
    print("==============[df1]================")
    df1 = pd.DataFrame({
        'name': ['Alice', 'Bob', 'Charlie']
    }, index=[1, 2, 3])
    print(df1)

    print("==============[df2]================")
    df2 = pd.DataFrame({
        'department': ['HR', 'Sales', 'IT']
    }, index=[2, 3, 4])
    print(df2)

    print("=======[concatinate()]==========")
    # default axis=0 vertical stacking
    print("====[vertical concatenation/stacking]====")
    print(pd.concat([df1, df2], axis=0, join='outer'))

    print("====[horizontal concatenation/stacking]====")
    print(pd.concat([df1, df2], axis=1, join='outer'))

    # NOTE:---------------------------------------------------------------
    # - pandas.concatenate(): is similar to the one in numpy, where we can
    #   make vertical/horizontal stacking between DataFrames.
    # - This Part is very important since we deal with data from different
    #   sources in real-life, SO THIS CONCEPT OF MEARING IS VERY VERY
    #   IMPORTANT.


if __name__ == '__main__':
    main()
