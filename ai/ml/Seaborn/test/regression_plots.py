def main():
    # display all available datasets in seaborn
    datasets = sns.get_dataset_names()
    print("========[Datasets]========")
    print(datasets)
    # load tips data base
    tips = sns.load_dataset('tips')
    print("======[Tips dataset]=======")
    print(tips)
    # Explore tips dataset
    print("=======[Quick Exploration]=======")
    tips.info()

    # 1. regplot(): visualizing the relationship between two variables
    # sns.regplot(x='tip', y='total_bill', data=tips)

    # 2. lmplot(): visualizing regression plots across categories
    sns.lmplot(x='total_bill', y='tip', data=tips, col='sex', hue='time')

    # NOTE:------------------------------------------------------------
    # You can clearly see that total_bill and tips is a strong positive
    # correlated

    plt.show()


if __name__ == '__main__':
    main()
