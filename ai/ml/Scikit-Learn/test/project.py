# ===[ Imports: ]==============================================================
import numpy as np
import matplotlib.pyplot as plt
from sklearn import datasets, linear_model, neighbors


# ===[ main: ]=================================================================
def main():
    # Load A Toy/Builting iris data for learning purposes
    iris_data = datasets.load_linnerud()  # return => `Bunch` Object
    # Extract Input features
    X = iris_data.data
    print(f"=======Input Features=======\n{X}")
    # Extract Target Labels
    y = iris_data.target
    print(f"=======Target Labels========\n{y}")
    # Model Selection
    mod = neighbors.KNeighborsRegressor()
    # Train Model
    mod.fit(X, y)
    # Predict Unseen data
    y_hat = mod.predict(X)
    print("======[Predicted y-hat]=====")
    print(y_hat)
    # Creating an Empy Figure
    fig = plt.figure()
    # add subplots/Axes to figure
    ax = fig.add_subplot(1, 1, 1)
    # plot in the axes
    ax.scatter(y, y_hat)
    plt.show()


if __name__ == '__main__':
    main()
