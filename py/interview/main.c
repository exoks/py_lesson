# include <stdlib.h>
# include <stdio.h>

typedef struct list {
  int content;
  struct list *next;
} t_list;

t_list *lst_new(int content) {
  t_list *node;

  node = malloc(sizeof(t_list));
  if (node == NULL)
    return (NULL);
  node->content = content;
  return (node);
}

int ft_power(int base, int power){
  if (power == 0)
    return (1);
  return (base * ft_power(base, --power));
}

int list_to_number(t_list *head) {
  int nbr;
  int power;

  nbr = 0;
  power = 0;
  while (head != NULL) {
    nbr += head->content * ft_power(10, power); 
    power++;
    head = head->next;
  }
  return(nbr);
}

