# ===[ Imports: ]==============================================================
import pandas as pd


# ===[ main ]==================================================================
def main():
    df = pd.read_csv(
        r"/home/oezzaou/py_lesson/ai/ml/Pandas/test/datasets/City_Types.csv")

    slice = df.loc[0:10, ['CO', 'NO2', 'O3']]
    print(f"slice: {slice}")
    print("=====[ Using describe()]=======")
    # discribe(): used for a quick aggregation overview
    # (statistical operations)
    print(slice.describe())

    # grp = slice.groupby(by='City')
    print('=================[mean()]==================')
    print(f"{slice.mean()}")
    print('========[std: standard deviation]===========')
    print(f"{slice.std()}")

    print("=====[ Aggregation: Apply a set of statistical operations]=======")
    # Using list aggregation of 'min', 'max', 'mean' on all my input features
    # [CO, NO2, O3]
    print(slice.agg(['min', 'max', 'mean']))
    # Using dictionary aggregation, apply statistical operations on specific
    # input feature
    print(slice.agg({
        'CO': ['min', 'max'],
        'NO2': 'mean',
        'O3': 'sum',
    }))

    # NOTE:--------------------------------------------------------------------
    # - Aggregation: is just a method to apply a group of statistical
    #   operations on a DataFrame
    # - You can either apply aggregation on grouped object (groupby()) or open
    #   DataFrame directly


if __name__ == '__main__':
    main()
