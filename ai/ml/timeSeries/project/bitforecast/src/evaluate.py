# ===[ Imports ]===============================================================
import matplotlib.pyplot as plt
from sklearn.metrics import mean_absolute_error, root_mean_squared_error
from sklearn.metrics import mean_absolute_percentage_error


# ===[ plot_forecasts ]========================================================
def plot_forecasts(train_set, test_set, forecasts, plot=False):
    # Creating an Empty figure
    fig = plt.figure(figsize=(10, 10))
    # Creating and axes / subplot in the figure
    ax = fig.add_subplot(1, 1, 1)
    # plot `train_set`
    ax.plot(train_set, label="train")
    # plot `test_set`
    ax.plot(test_set, label="test")
    # plot `forecasts`
    ax.plot(forecasts, label="forecasts")
    # add legend (do not forget to add labels)
    ax.legend()
    plt.show()


# ===[ compute_metrics ]=======================================================
def compute_metrics(actuals, forecasts):
    mae = mean_absolute_error(actuals, forecasts)
    rmse = root_mean_squared_error(actuals, forecasts)
    mape = mean_absolute_percentage_error(actuals, forecasts)

    metrics = {
        "mae": mae,
        "rmse": rmse,
        "mape": mape * 100,  # convert from propertions to percentage
    }
    return metrics


# ===[ evaluate_model ]========================================================
def evaluate_model(model, train_set, test_set, plot=False):
    # 1. train the model
    train_model = model.fit()
    # 2. predict/forecast `test_set`
    forecasts = train_model.forecast(len(test_set))
    # 3. compute metrics
    metrics = compute_metrics(test_set, forecasts)
    # 4. plot forecasts
    plot_forecasts(train_set['2023-05'], test_set, forecasts, plot=True)
    # NOTE:----------------------------------------------------------------
    # A big gap between 'trained' and 'test data' is shown
    return forecasts, metrics
