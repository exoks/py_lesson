# ===[ Imports ]===============================================================
import numpy as np
import matplotlib.pyplot as plt
import matplotlib.gridspec as gs


# ===[ ft_subplots ]===========================================================
def ft_subplots(nrows, ncols):
    # Create A Figure an Empty Figure
    fig = plt.figure(figsize=(nrows, ncols))
    # Creating A GridSpec Layout helper for 'fig'
    grid = gs.GridSpec(nrows, ncols, figure=fig)
    # Creating a numpy array that holds all subplots/axes objects
    axes = np.empty((nrows, ncols), dtype=object)
    # Filling the Array
    for row in range(nrows):
        for col in range(ncols):
            axes[row][col] = fig.add_subplot(grid[row, col])
    return fig, axes


# ==[ main ]===================================================================
def main():
    # Creating A figure and 4 subplots/Axes all at once
    fig, axs = ft_subplots(2, 3)
    # fig: is the figure object
    # axs is numpy array that holds all axes/subplots, It is a 2D array/Matrix
    # of size 2x2
    plt.show()


if __name__ == '__main__':
    main()
