import numpy as np
import statsmodels.api as sm

y = np.array([1, 0, 2, 1, 3, 2])


def pacf_lag2(y):
    # Step 1: Align series
    yt = y[2:]
    y1 = y[1:-1]  # lag 1
    y2 = y[:-2]   # lag 2

    # Step 2: Regress yt on y1
    X1 = sm.add_constant(y1)
    res1 = yt - sm.OLS(yt, X1).fit().fittedvalues

    # Step 3: Regress y2 on y1
    res2 = y2 - sm.OLS(y2, X1).fit().fittedvalues

    # Step 4: Correlate residuals
    return np.corrcoef(res1, res2)[0, 1]


print("PACF(2) =", pacf_lag2(y))

