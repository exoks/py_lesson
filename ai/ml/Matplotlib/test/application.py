# ===[ Imports: ]==============================================================
import sklearn.datasets as datasets
import matplotlib.pyplot as plt


# ===[ main: ]=================================================================
def main():
    # Load a set of Datasets
    dt = datasets.load_digits()
    # print the type of the dataset
    print("=========[images]==========")
    print(dt.images)
    # each image represent a matrix, the 3D array / Tensor
    print(type(dt.images))  # a 3D numpy array/ Tensor
    # Indexing the first image
    first_image = dt.images[0]
    # visualizing the image using the imshow() ploting function from matplotlib
    plt.imshow(first_image)

    # Creating a Figure and 10 subplots/axes in grid of 10x10
    fig, axes = plt.subplots(5, 5)

    # loop over each cell grid/subplot/axes and plot
    print("=================================")
    for index, ax in enumerate(axes.flat):
        ax.imshow(dt.images[index], cmap='binary')
        # Remove numbers from X-axis & Y-axis
        ax.set_xticks([])
        ax.set_yticks([])

    plt.show()


if __name__ == '__main__':
    main()
