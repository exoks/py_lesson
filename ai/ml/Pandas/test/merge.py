# ===[ Imports: ]==============================================================
import pandas as pd


# ===[ main ]==================================================================
def main():

    print("======================[df1]======================")
    df1 = pd.DataFrame({
        'employee_id': [1, 2, 3],
        'name': ['Alice', 'Bob', 'Charlie']
    })
    print(df1)

    print("======================[df2]======================")
    df2 = pd.DataFrame({
        'employee_id': [2, 3, 4],
        'department': ['Sales', 'IT', 'HR']
    })
    print(df2)

    print("===========[inner merge (intersection)]=============")
    # 1. inner merge (default)
    # It will merge columns (input feature)
    print(pd.merge(df1, df2, how='inner'))

    print("========[outer merge (union)]==========")
    # 2. outer merge
    # It will merge columns (input feature)
    print(pd.merge(df1, df2, how='outer'))

    print("===========[ left merge ]=============")
    # 2. outer merge
    # preserve df1 + intersection merge
    print(pd.merge(df1, df2, how='left'))

    print("===========[ right merge ]=============")
    # 2. right merge
    # preserve df2 + intersection merge
    print(pd.merge(df1, df2, how='right'))

    # NOTE:----------------------------------------------------------------
    # - merge(): it does merge based on columns/labels (COLUMN-BASED JOINS)


if __name__ == '__main__':
    main()
