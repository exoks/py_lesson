# ===[ ft_split: ]=============================================================
def ft_split(s: str) -> list:
    seen = set()
    splited_s = s.split(' ')

    output = []
    for word in splited_s:
        if word not in seen:
            print(seen)
            seen.add(word)
            output.append(word)
    return output


