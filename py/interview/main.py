def transportMatrix(matrix: list):
    rows, cols = len(matrix), len(matrix[0])
    reversed = []

    for col in range(cols):
        tmp = []
        for row in range(rows):
            tmp.append(matrix[row][col])
        reversed.append(tmp)
    return reversed


def main():
    matrix = [
        [1, 2, 3],
        [4, 5, 6],
    ]

    rev_matrix = transportMatrix(matrix)
    print(rev_matrix)


if __name__ == '__main__':
    main()
