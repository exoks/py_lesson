# ===[ Imports: ]==============================================================
import pandas as pd


# ===[ main ]==================================================================
def main():
    print("==============[df1]================")
    df1 = pd.DataFrame({
        'name': ['Alice', 'Bob', 'Charlie']
    }, index=[1, 2, 3])
    print(df1)

    print("==============[df2]================")
    df2 = pd.DataFrame({
        'department': ['HR', 'Sales', 'IT']
    }, index=[2, 3, 4])
    print(df2)

    print("===============[join]============")
    print("=======[how=left]======")
    # default (how=left)
    print(df1.join(df2, how='left'))
    print("=======[how=right]======")
    # how=right
    print(df1.join(df2, how='right'))
    print("=======[how=inner]======")
    # how=inner
    print(df1.join(df2, how='inner'))
    print("=======[how=outer]======")
    # how=outer
    print(df1.join(df2, how='outer'))

    # NOTE:----------------------------------------------------------------
    # As you can see joining/mergin by index, unlike merge that joins bytes
    # columns (INDEX-BASED JOINS)


if __name__ == '__main__':
    main()
